class WatchRunPlugin {
    apply(compiler) {
        compiler.hooks.watchRun.tap('WatchRun', (comp) => {
            if (comp.modifiedFiles) {
                const changedFiles = Array.from(comp.modifiedFiles, (file) => `\n  ${file}`).join('');
                console.log('');
                console.log('===============================');
                console.log('FILES CHANGED:', changedFiles);
                console.log('===============================');
            }
            if (comp.removedFiles) {
                const changedFiles = Array.from(comp.removedFiles, (file) => `\n  ${file}`).join('');
                console.log('');
                console.log('===============================');
                console.log('FILES REMOVED:', changedFiles);
                console.log('===============================');
            }
        });
    }
}

class SetupPlugin {
    constructor(mode) {
        this.mode = (mode === 'development') ? "-dev" : ""
    }
    apply(compiler) {
        compiler.hooks.beforeCompile.tap('Setup',async(comp) =>{
            const configObj = await require(`./config/${process.env.VE_ENV}.json`)

            await fs.writeFile(`${experimentalDir}/index.ts`, '//** Automatically Generated by Webpack do not edit **//', (err) => {
                if (err) {
                    throw err;
                }}
            );

            if (configObj.experiments) {
                let validExt = ['specTools', 'transclusions', 'presentations'];
                for (let exp of configObj.experiments) {
                    let expPath = `./${exp.id}`
                    if (exp.path) {
                        expPath = path.resolve(sourceDir, extType, exp.path);
                    }

                    fs.appendFile(`${experimentalDir}/index.ts`,`\nimport '${expPath}'`)
                    if (!configObj.expConfig) {
                        configObj.expConfig = {specTools: [], transclusions: [], presentations: []}
                    }
                    let expConfPath = `${experimentalDir}/${exp.id}/config.`
                    if (exp.config) {
                        expConfPath = exp.config
                    }
                    let expConf = await require(expConfPath)
                    for (let extType of validExt) {
                        if (expConf[extType].length > 0) {
                            for (let ec of expConf[extType]) {
                                configObj.expConfig[extType].push(expConf);
                            }
                        }
                    }
                }
            }
            await packageJson
            configObj.version = `${packageJson.version}${this.mode}`;
            fs.writeFile('./dist/config/config.json', JSON.stringify(configObj), (err) => {
                if (err) {
                    throw err;
                }});
        })
    }
}

